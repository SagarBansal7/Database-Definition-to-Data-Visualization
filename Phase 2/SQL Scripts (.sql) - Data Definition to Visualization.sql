--CREATE TABLES

create table trainer
(employee_number varchar2(30) not null,
first_name varchar2(10) not null,
middle_name varchar2(15),
last_name varchar2(15),
Street1 varchar2(30),
Street2 varchar2(15),
City varchar2(15),
State varchar2(2),
Zip varchar2(5),
employee_phone_number varchar2(15),
email_address varchar2(15),
employment_start_date Date,
employment_end_date Date,
constraint pk_trainer primary key (employee_number));


create table hardware
(hardware_id varchar2(30) not null,
hardware_name varchar2(30),
hardware_description varchar2(50),
operating_system varchar2(30),
constraint pk_hardware primary key (hardware_id));


create table classroom
(classroom_number varchar2(30) not null,
classroom_capacity number(10,0),
hardware_id varchar2(30) not null,
constraint pk_classroom primary key (classroom_number ),
constraint fk_classroom_hardware foreign key (hardware_id) references hardware (hardware_id));


create table software
(software_id varchar2(30) not null,
software_name varchar2(30),
software_publisher varchar2(30),
number_of_licenses number(10,0),
constraint pk_software primary key (software_id));


create table hardware_software 
(hardware_id varchar2(30) not null,
software_id varchar2(30) not null, 
constraint pk_hardware_software primary key (hardware_id,software_id),
constraint fk_hardware_software1 foreign key (hardware_id) references hardware(hardware_id),
constraint fk_hardware_software2 foreign key (software_id) references software(software_id));


create table client
(client_number varchar2(30) not null,
first_name varchar2(10),
middle_name varchar2(15),
last_name varchar2(15),
Street1 varchar2(30),
Street2 varchar2(15),
City varchar2(15),
State varchar2(2),
Zip varchar2(5),
phone_number varchar2(20),
initial_date date not null,
constraint pk_client primary key (client_number));


create table membership
(membership_code varchar2(1) not null,
membership_type varchar2(30),
membership_duration  varchar2(10),
membership_price number(11, 2),
discount number(5, 2),
constraint pk_membership primary key (membership_code));


create table membership_purchase
(membership_purchase_id varchar(10) not null,
client_number varchar2(30) not null,
membership_code varchar2(1) not null,
membership_payment number(10,2),
date_of_assignment date,
constraint pk_membership_purchase primary key (membership_purchase_id),
constraint fk_client_purchase foreign key (client_number) references client (client_number),
constraint fk_mem_purchase foreign key (membership_code) references membership (membership_code));


create table author
(author_id varchar2(30) not null,
first_name varchar2(10),
middle_name varchar2(15),
last_name varchar2(15),
constraint pk_author primary key (author_id));


create table course_book
(course_book_isbn varchar2(20) not null,
book_title varchar2(30),
publisher varchar2(20),
list_price number(10, 2),
constraint pk_course_book primary key (course_book_isbn));


create table author_book_listing
(author_id varchar2(30) not null,
course_book_isbn varchar2(20) not null,
constraint pk_author_book_listing primary key (author_id, course_book_isbn),
constraint fk_author_list foreign key (author_id) references author (author_id),
constraint fk_coursebook_list foreign key (course_book_isbn) references course_book (course_book_isbn));


create table course
(unique_code varchar2(30) not null,
course_length number(10,0),
course_fee number(10,2),
course_level varchar2(20),
course_book_isbn varchar2(20),
hardware_id varchar2(30) not null,
constraint pk_course primary key (unique_code),
constraint fk_course_hardware foreign key (hardware_id) references hardware (hardware_id),
constraint fk_course_coursebook foreign key (course_book_isbn) references course_book (course_book_isbn));



create table qualification
(Qualification_id varchar2(30) not null,
employee_number varchar2(30) not null,
unique_code varchar2(30) not null,
date_of_qualification date,
constraint pk_qualification primary key (Qualification_id),
constraint fk_qualification_trainer foreign key (employee_number) references trainer (employee_number),
constraint fk_qualification_course foreign key (unique_code) references course (unique_code));


create table course_prerequisites
(unique_code varchar2(30) not null,
prerequisite_course_code varchar2(30) not null,
constraint pk_prerequisites_course primary key (unique_code, prerequisite_course_code),
constraint fk_course_pre foreign key (unique_code) references course (unique_code),
constraint fk_course_pre_pre foreign key (prerequisite_course_code) references course (unique_code));

create table course_software
(unique_code varchar2(30) not null,
software_id varchar2(30) not null, 
constraint pk_course_software primary key (unique_code,software_id),
constraint fk1_course_software foreign key (unique_code) references course(unique_code),
constraint fk2_course_software foreign key (software_id ) references software(software_id ));

create table class
(reference_number varchar2(10) not null,
start_date date,
end_date date,
employee_number varchar2(30),
classroom_number varchar2(10),
unique_code varchar2(30),
constraint pk_class primary key (reference_number),
constraint fk_class_trainer foreign key (employee_number) references trainer (employee_number),
constraint fk_class_classroom foreign key (classroom_number) references classroom (classroom_number),
constraint fk_class_course foreign key (unique_code) references course (unique_code));

create table enrollment_request
(enrollment_id varchar2(10) not null,
request_date_time timestamp(0),
status varchar2(10) not null,
payment number(10,2),
reference_number varchar2(10),
client_number varchar2(30),
constraint pk_enrollment_request primary key (enrollment_id),
constraint fk_enroll_class foreign key (reference_number) references class (reference_number),
constraint fk_enroll_client foreign key (client_number) references client (client_number));


create table vendor
(vendor_id varchar2(10) not null,
vendor_name varchar2(30),
Street1 varchar2(30),
Street2 varchar2(15),
City varchar2(15),
State varchar2(2),
Zip varchar2(5),
phone_number varchar2(15),
constraint pk_vendor primary key (vendor_id));


create table ordert
(order_number varchar2(20) not null,
order_date date,
order_status  varchar2(10),
vendor_id varchar2(10) not null,
constraint pk_ordert primary key (order_number),
constraint fk_ordert_vendor foreign key (vendor_id) references vendor (vendor_id));


create table shipment
(shipment_id varchar2(10) not null,
first_name varchar2(30) not null,
middle_name varchar2(15),
last_name varchar2(15),
Street1 varchar2(30) not null,
Street2 varchar2(15) ,
City varchar2(15) not null,
State varchar2(2) not null,
Zip varchar2(5) not null,
shipment_date date,
vendor_id varchar2(10) not null,
constraint pk_shipment primary key (shipment_id),
constraint fk_shipment_vendor foreign key (vendor_id) references vendor (vendor_id));


create table order_line
(order_number varchar2(10) not null,
course_book_isbn varchar2(20) not null,
quantity_ordered number(10,0),
price_charged number(10,2),
constraint pk_orderline primary key (order_number, course_book_isbn),
constraint fk1_orderline foreign key (order_number) references ordert (order_number),
constraint fk2_orderline foreign key (course_book_isbn) references course_book (course_book_isbn));


create table shipment_line
(shipment_id varchar2(10) not null,
course_book_isbn varchar2(20) not null,
quantity_received number(10,0),
constraint pk_shipmentline primary key (shipment_id, course_book_isbn),
constraint fk1_shipmentline foreign key (shipment_id) references shipment(shipment_id),
constraint fk2_shipmentline foreign key (course_book_isbn) references course_book (course_book_isbn));

create table course_book_vendor
(course_book_isbn varchar2(20) not null,
vendor_id varchar2(10) not null,
constraint fk1_coursebook_vendor foreign key (course_book_isbn) references course_book(course_book_isbn),
constraint fk2_coursebook_vendor foreign key (vendor_id) references vendor(vendor_id));

